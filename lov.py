async def handle_message(update: Update, context: ContextTypes.DEFAULT_TYPE):
    """–û–±—Ä–∞–±–æ—Ç—á–∏–∫ –≤—Å–µ—Ö —Ç–µ–∫—Å—Ç–æ–≤—ã—Ö —Å–æ–æ–±—â–µ–Ω–∏–π (–∫—Ä–æ–º–µ –∫–æ–º–∞–Ω–¥)"""
    user_id = update.effective_user.id
    user_data = get_user(user_id)
    now = time.time()
    message_text = update.message.text.strip()

    # –ï—Å–ª–∏ –¥–æ—Å—Ç—É–ø —É–∂–µ –µ—Å—Ç—å, –∏–≥–Ω–æ—Ä–∏—Ä—É–µ–º –æ–±—ã—á–Ω—ã–µ —Å–æ–æ–±—â–µ–Ω–∏—è
    if user_data["access"]:
        # –ü–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—å —Å –¥–æ—Å—Ç—É–ø–æ–º –º–æ–∂–µ—Ç –ø–∏—Å–∞—Ç—å —á—Ç–æ —É–≥–æ–¥–Ω–æ, –Ω–æ —Ç–æ–ª—å–∫–æ –∫–æ–º–∞–Ω–¥—ã –æ–±—Ä–∞–±–∞—Ç—ã–≤–∞—é—Ç—Å—è
        # –û–±—ã—á–Ω—ã–µ —Å–æ–æ–±—â–µ–Ω–∏—è –∏–≥–Ω–æ—Ä–∏—Ä—É–µ–º
        return

    # –ü—Ä–æ–≤–µ—Ä–∫–∞ –±–ª–æ–∫–∏—Ä–æ–≤–∫–∏
    if user_data["blocked_until"] > now:
        await update.message.reply_text("‚õî –°–ª–∏—à–∫–æ–º –º–Ω–æ–≥–æ –ø–æ–ø—ã—Ç–æ–∫. –ñ–¥–∏—Ç–µ.")
        return

    # –ü—Ä–æ–≤–µ—Ä–∫–∞ –ø–∞—Ä–æ–ª—è
    if message_text == PASSWORD:
        user_data["access"] = True
        user_data["attempts"] = 0
        await update.message.reply_text("‚úÖ –ü–∞—Ä–æ–ª—å –≤–µ—Ä–Ω—ã–π. –î–æ—Å—Ç—É–ø –æ—Ç–∫—Ä—ã—Ç\n\n–¢–µ–ø–µ—Ä—å –º–æ–∂–µ—Ç–µ –∏—Å–ø–æ–ª—å–∑–æ–≤–∞—Ç—å –∫–æ–º–∞–Ω–¥—É /loveRomatik")
    else:
        user_data["attempts"] += 1
        remaining_attempts = MAX_ATTEMPTS - user_data["attempts"]
        
        if user_data["attempts"] >= MAX_ATTEMPTS:
            user_data["blocked_until"] = now + BLOCK_TIME
            user_data["attempts"] = 0
            await update.message.reply_text("‚õî 5 –æ—à–∏–±–æ–∫. –ë–ª–æ–∫ –Ω–∞ 10 —á–∞—Å–æ–≤.")
        else:
            await update.message.reply_text(f"‚ùå –ù–µ–≤–µ—Ä–Ω—ã–π –ø–∞—Ä–æ–ª—å. –û—Å—Ç–∞–ª–æ—Å—å –ø–æ–ø—ã—Ç–æ–∫: {remaining_attempts}")

